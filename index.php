  <p>L'examen d’entrée de l’Ensba Lyon est ouvert à tou·tes les candidat·es français·es ou étranger·es titulaires du baccalauréat ou d’un diplôme équivalent.</p>
  <p>Le-a candidat·e peut s’inscrire à l'examen d'entrée bien qu’étant en terminale, son admission définitive sera soumise à la réussite du baccalauréat. Les étudiant·es de classe préparatoire aux écoles d’art peuvent évidemment postuler à l'examen d’entrée.</p>
  <p>Une dérogation exceptionnelle peut être accordée par la directrice de l’Ensba Lyon pour les candidat·es non bachelier·es (sortis du système scolaire) sur demande expresse auprès du Service des études et de la scolarité.</p>
  <p>Montant du droit à l'examen : 50 €</p>
  <p>Pour toute question relative aux admissions : </p>
  <p>candidature@ensba-lyon.fr</p>
