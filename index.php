<h2>Examen d'entrée 1re année</h2>
  <p>Dans la situation actuelle particulière, liée à la crise sanitaire, l’Ensba se réserve le droit de modifier les modalités de l’examen d’entrée en première année afin que les candidat·es puissent le passer dans les meilleures conditions.</p>
