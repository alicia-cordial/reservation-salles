<h1>LES BEAUX ARTS DE PARIS</h1>

    <div class="divider"></div>
        <div class="section1">
        <h5>Les missions de l’École</h5>
            <p>Les Beaux-Arts de Paris sont à la fois un lieu de formation et d’expérimentations artistiques, d’expositions et de conservation de collections historiques et contemporaines et une maison d’édition. Héritière des Académies royales de peinture et de sculpture fondées au XVIIème siècle par Louis XIV, l’École nationale supérieure des Beaux-Arts, placée sous la tutelle du ministère de la Culture, a pour vocation première de former des artistes de haut niveau. Elle occupe une place essentielle sur la scène artistique contemporaine. Conformément aux principes pédagogiques qui ont toujours eu cours aux Beaux-Arts, la formation y est dispensée en atelier, sous la conduite d’artistes de renom. Cette pratique d’atelier est complétée par une palette d’enseignements théoriques et techniques qui ont pour but de permettre aux étudiants une diversité d’approches. Ils visent à leur apporter une large culture artistique, tout en favorisant la multiplicité des champs d’expérimentation et la transdisciplinarité.</p>
            <p>Les Beaux-Arts de Paris sont partenaires de l’université Paris Sciences & Lettres (PSL), un regroupement d’universités qui comprend 25 établissements prestigieux de la capitale. </p>

        </div>

