        <div class="section2">
            <h5>Saint-Germain-des-Prés et Saint-OuenE</h5>
                <p>Les Beaux-Arts de Paris sont implantés sur deux sites, à Saint-Germain-des-Prés et à Saint-Ouen.</p>
                <p>L’institution parisienne des Beaux-Arts, située à Saint-Germain-des-Prés, se répartit sur deux hectares. Elle comprend de nombreux ateliers, auxquels s’ajoutent trois amphithéâtres, une bibliothèque spécialisée en art contemporain, ainsi que deux espaces d’exposition, le Palais des Beaux-Arts et le cabinet des dessins Jean Bonna.</p>
                <p>Depuis 2008, les Beaux-Arts de Paris disposent d’une seconde implantation à Saint-Ouen. Un vaste espace situé dans le quartier des Puces, abrite les ateliers et pratiques de taille, mosaïque, modelage, moulage, moulage, forge, matériaux composites et céramique. Hébergé dans un ancien bâtiment industriel s’étendant sur près de 1 000 m², le site de Saint-Ouen permet de développer des projets de grande envergure.</p>
                <p>Il accueille également la nouvelle classe préparatoire publique, inaugurée en septembre 2016. Intitulée Via Ferrata , elle reçoit vingt-cinq étudiants issus de lycées placés en zone prioritaire, désireux de se préparer aux concours d’entrée aux écoles d’art. Cette classe bénéficie du soutien de la Fondation SNCF et de Gide Pro Bono..</p>
        </div>

