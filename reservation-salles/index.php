        <div class="section3">
            <h5>Histoire des bâtiments</h5>
                <p>Les Beaux-Arts de Paris sont implantés sur deux sites, à Saint-Germain-des-Prés et à Saint-Ouen.</p>
                <p>Les Beaux-Arts de Paris forment un vaste ensemble architectural dont les bâtiments, répartis entre la rue Bonaparte et le quai Malaquais, datent des XVIIème, XVIIIème , XIXème et XXème siècles. L’institution, héritière directe des Académies royales de peinture et de sculpture, fondée par Mazarin en 1648, sera dissoute par la Convention en 1793. Sous l’Empire, l’École académique et l’Académie d’architecture fusionnent en une seule institution, donnant naissance à l’École des Beaux- Arts.</p>
                <p>Celle-ci est successivement installée au Louvre, au Collège des Quatre-Nations, rue Mazarine, puis, sur le site de l’ancien couvent des Petits-Augustins, rue Bonaparte. La chapelle et ses bâtiments annexes, élevés au début du XVIIème siècle pour le couvent des Petits-Augustins, constituent les constructions les plus anciennes de l’École. </p>
                <p>En 1790, Alexandre Lenoir, conservateur, y aménage le musée des Monuments français et rassemble des copies des plus beaux édifices du pays ainsi que des éléments de sculptures remarquables. Durant la Révolution y sont rapatriés les œuvres et monuments en péril, menacés de destruction en raison de leurs marques d’appartenance à l’ordre religieux ou monarchique. En 1816, avec le retour de la monarchie, Louis XVIII fait fermer le musée. Les œuvres religieuses sont restituées aux églises, certains monuments originaux remis en place (Tombeaux des rois) et d’autres laissés sur place. C’est alors que les lieux sont affectés à l’École des Beaux-Arts qui, depuis 1829, occupe son emplacement actuel. L’installation de l’École donne lieu à un projet architectural ambitieux. L’architecte François Debret (1777-1850) est chargé d’y édifier de nouveaux locaux. Il fait construire le bâtiment des Loges, pour servir au déroulement des concours et conçoit le Palais des études. Félix Duban (1797-1872), son élève et beau-frère, parachèvera son œuvre. Il termine la construction du Palais des études et fait édifier le bâtiment des expositions (comprenant les salles Melpomène et Foch) qui donne sur le quai Malaquais. Enfin, il aménage les cours d’entrée côté rue Bonaparte, ainsi que la chapelle et le cloître (cour du mûrier) de l’ancien couvent. Le musée des Monuments français a profondément marqué son temps et constitue une source d’inspiration importante pour Félix Duban, qui réutilise des éléments architecturaux et décoratifs, parfois disparates, restés en place après la dispersion des collections du musée. Il conçoit ainsi un ensemble original, dont l’attrait et la curiosité tiennent aussi à son incontestable unité. C’est en 1883 que l’École connaît sa dernière grande extension avec l’achat de l’hôtel de Chimay et de ses annexes des XVIIème et XVIIIème siècles, situés aux 15 et 17 quai Malaquais. Elle trouve alors sa forme définitive.</p>
